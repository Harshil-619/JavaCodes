public class WhileLoop {

    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println("I love JAVA");
            i++;
        }
        int j = 0;
        do {
            System.out.println("I love Programming");
            j++;
        } while (j < 5);
    }

}
