public class SwitchCase {
    public static void main(String[] args) {
        int num = 7;

        switch (num) {
            case 1:
                System.out.println("SOMWAAR");
                break;
            case 2:
                System.out.println("Mangalwaar");
                break;
            case 3:
                System.out.println("Buudhwaar");
                break;
            case 4:
                System.out.println("Guruwaar");
                break;
            case 5:
                System.out.println("Shukrawaar");
                break;
            case 6:
                System.out.println("Shaniwaar");
                break;
            case 7:
                System.out.println("RAVIVAAR");
                break;
            default:
                System.out.println("Invalid Input");

        }
    }
}
